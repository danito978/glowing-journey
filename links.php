                    <h4 style="color:white;">Audio</h4>
                    <p style="color:white;">
<a href="http://ntonyx.com/vac.html">VirtualAudioCables</a>: Useful for segregating audio streams. Example: you can listen to music or talk in a mic without it being in your stream if you so choose. This is a paid version. Free version with less features found here.
<br><br>
<a href="https://auto-duck.com/">AutoDuck</a>:  Allows real time ducking of audio from another audio source. example: when you talk in a mic it can fade out your music and game volume. Has a free-trail then you must pay.
<br><br>
<a href="https://github.com/dlrudie/Snip/releases/tag/v4.6.0">Snip</a>: You can use it to display song information (track name, artist) on your stream when listening to e.g. spotify. </p>
