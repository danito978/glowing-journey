                <div class="col-md-12">
                    <h4 style="color:white;">Programs to steam with or record</h4>
                    <p style="color:white;"><a href="https://obsproject.com/">OBS</a>: Arguably the most common streaming software on Twitch. It has many features built in as well as the option to expand with plugins.
                        <br><br>
<a href="https://www.xsplit.com/">X-Split</a>: The paid streaming software that has a more polished surface, but seems to be lacking in features as OBS grows.
<br><br>
<a href="https://github.com/mishira/mishira">Mishira</a>: A PC dedicated streaming software currently in Alpha. It is unknown if the developer has dropped it, but it does have some nice capabilities.
<br><br>
<a href="https://www.ffsplit.com/">FFS-Split</a>: Another streaming service that is not that common, but will get the job done.
<br><br>
<a href="https://exkode.com/dxtory-features-en.html">Dxtory</a>: (Dxtory's Directshow output into OBS/Xsplit is a far better solution than using either programs 
fullscreen game capture alone. It is less resource intensive by far. It also allows you to full screen capture games and output into FFsplit (which otherwise doesn't
have full screen game support). The other advantage is that this method allows you to use the full capabilities of your SLI or CFX system where as the straight OBS/Xsplit gamecapture will be a huge hit in performance. </p>
                </div>
