             <div class="row">
                <div class="col-md-12">
                    <h4 style="color:white;">Twitch Bots</h4>
                    <p style="color:white;">Xanbot: A twitch bot made by one of the staff themselves. This bot has quite a few features, but it is only controlled through chat. (Here is a list of commands http://xangold.tv/help/)
                        <br><br>
<a href="https://beta.nightbot.tv/">Nightbot</a>: One of the most common bots. A great moderation bot with a web dashboard for convenience. Nightbot also has a few extra features found here https://wiki.nightbot.tv/chat:variables
<br><br>
<a href="https://moo.bot/">Moobot</a>: Similar to Nightbot with a few design and feature changes. Some extra things are accessible with points that can be paid for.
<br><br>
<a href="http://gamerabes.tv/abesbot">AbesBot</a>: An up and coming bot featuring a system for chat commands to on screen animations and sounds. It is currently the first public bot to accomplish this.
<br><br>
<a href="http://deepbot.deep.sg/">Deepbot</a>: A desktop bound bot rich in features. This one will set you back $15.00 unless you meet certain criteria.
<br><br>
<a href="https://sourceforge.net/projects/twitchmodbot/">ModBot</a>: A desktop program loyalty bot.
<br><br>
<a href="https://github.com/SupremoRTD/LoyaltyBot">LoyaltyBot</a>: A slightly more complex web server based loyalty bot.
<br><br>
<a href="https://github.com/Fasani/SpottyBot">SpottyBot</a>: A bot built for playing VOD approved music through spotify. 
<br><br>
<a href="https://github.com/DarkAutumn/WinterBot">WinterBot</a>: A desktop based bot that is very similar to Nightbot.
<br><br>
<a href="https://www.scias.net/deejbot/">DeejiBot</a>: Not that I recommend it, but if you desire this is a greeter bot.
<br><br>
<a href="https://bashtech.net/twitch/geobot.php">GeoBot</a>: Similar to Xanbot
<br><br>
<a href="http://ww12.marcinswierzowski.com/">Ankhbot</a>: This bot is desktop bound and quite similar to Deepbot. It is currently free.
<br><br>
<a href="https://www.twitch.tv/analyticsbot/videos">AnalyticsBot</a>: A bot built to assist you with stream information. </p>
