                    <h4 style="color:white;">Desktop Programs</h4>
                    <p style="color:white;">
<a href="http://getchatty.sourceforge.net/">Chatty</a>: A convenient IRC tool for Twitch
<br><br>
<a href="https://obsproject.com/forum/resources/teeboard.45/">TeeBoard</a>: Known as the swiss army knife of Twitch tools. Comes with numerous functions to help out streamers.
<br><br>
<a href="http://tards.net/">Tardsplaya w</a>:  An extremely useful tool for resolving difficulty in viewing non-partnered streams.
<br><br>
<a href="https://binx.tv/">Bink Stream Assist</a>: An interesting application with a variety of features for stream details and editing. Similar to Teeboard in some ways with its own unique features. The download is located in the support page (magnifying glass icon)
